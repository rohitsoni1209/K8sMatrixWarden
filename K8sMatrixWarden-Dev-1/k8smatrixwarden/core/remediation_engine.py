"""
Remediation Validation Engine — schema-aware, resource-aware remediation generation.

The bug this exists to fix: remediation was previously generated by formatting a single
hardcoded command STRING per playbook (e.g. always assuming `spec.template.spec`), keyed
only by the finding's `remediation_ref`. That produces syntactically-valid-but-rejected
commands whenever the actual target doesn't have that shape — a bare Pod has `spec`, not
`spec.template.spec`; a CronJob's pod template lives even deeper, at
`spec.jobTemplate.spec.template.spec`. Worse, a controller-owned Pod (the overwhelmingly
common case in a real cluster) would get a `kubectl patch pod ...` command when the fix
needs to go on the Deployment/DaemonSet/StatefulSet that owns it — Pods are largely
immutable once created, so that patch would be rejected outright.

Pipeline (mirrors the requested design 1:1):

    Finding -> resolve resource -> resolve owner -> resolve deployment method ->
    determine mutable field -> determine correct patch path -> validate schema ->
    generate remediation -> validate command -> return remediation

Owner resolution itself happens earlier, at finding-creation time in the owning shard
(see shards/workload_pod_security.py's `ref(ev, res)`), where the Evidence needed to walk
ReplicaSet->Deployment / Job->CronJob is actually available — by the time a Finding
reaches this engine, `finding.resource.owner_kind/owner_name/labels/annotations` are
already resolved as far as the scan could confirm. This engine is a pure function of the
Finding; it never re-touches the cluster.

Accuracy over coverage: whenever any step can't be resolved safely, `generate()` returns
`automatable=False` with a `reason_not_automated` explanation instead of guessing. A
command Kubernetes silently ignores or rejects is worse than admitting "manual review
required".
"""
from __future__ import annotations

import dataclasses
import json
import re
from dataclasses import dataclass, field as _field
from typing import Any, Optional

from .models import Finding, ResourceRef

# --------------------------------------------------------------------------------- #
# Schema knowledge: where does each kind's Pod template actually live?
# --------------------------------------------------------------------------------- #
#: Path *prefix segments* to the PodSpec root for each kind. A FieldPatch's `path` is
#: relative to this — e.g. path="securityContext" on a Deployment means the real API
#: field is spec.template.spec.securityContext; on a CronJob it's
#: spec.jobTemplate.spec.template.spec.securityContext.
POD_SPEC_PATH_PREFIX: dict[str, list[str]] = {
    "Pod": ["spec"],
    "Deployment": ["spec", "template", "spec"],
    "DaemonSet": ["spec", "template", "spec"],
    "StatefulSet": ["spec", "template", "spec"],
    "ReplicaSet": ["spec", "template", "spec"],
    "Job": ["spec", "template", "spec"],
    "CronJob": ["spec", "jobTemplate", "spec", "template", "spec"],
}

#: Controllers whose Pod template can be safely patched — Kubernetes reconciles new Pods
#: from it (a rollout for Deployment/DaemonSet/StatefulSet, future scheduled runs for
#: CronJob). Patching one of these is never rejected for a well-formed field patch.
CONTROLLER_KINDS_PATCHABLE = {"Deployment", "DaemonSet", "StatefulSet", "CronJob"}

#: A Pod's *direct* owner is one of these when it is NOT itself the top-level controller.
#: If shards/workload_pod_security.py couldn't resolve past them (the intermediate
#: object wasn't in the scan's evidence, or it genuinely has no further owner), we land
#: here — and deliberately do NOT guess a rename/target from it (see _validate).
UNRESOLVED_INTERMEDIATE_KINDS = {"ReplicaSet", "Job"}


# --------------------------------------------------------------------------------- #
# System-workload detection (§5)
# --------------------------------------------------------------------------------- #
SYSTEM_NAMESPACES = {"kube-system", "kube-public", "kube-node-lease"}
SYSTEM_NAME_MARKERS = (
    "aws-node", "coredns", "kube-dns", "kube-proxy", "ebs-csi", "efs-csi",
    "metrics-server", "cluster-autoscaler", "vpc-cni", "calico", "cilium",
    "konnectivity",
)

SYSTEM_WORKLOAD_WARNING = (
    "This appears to be a Kubernetes/EKS managed component. Verify vendor "
    "documentation before changing its security context because the recommended "
    "hardening may break cluster functionality."
)


def is_system_workload(namespace: Optional[str], name: Optional[str]) -> bool:
    if (namespace or "").lower() in SYSTEM_NAMESPACES:
        return True
    low = (name or "").lower()
    return any(marker in low for marker in SYSTEM_NAME_MARKERS)


# --------------------------------------------------------------------------------- #
# Deployment-method detection (§6)
# --------------------------------------------------------------------------------- #
def detect_deployment_method(labels: Optional[dict], annotations: Optional[dict]) -> str:
    """Return 'helm' | 'argocd' | 'flux' | 'kubectl' from ownership/tracking markers."""
    labels = labels or {}
    annotations = annotations or {}
    keys = [str(k) for k in list(labels) + list(annotations)]
    if str(labels.get("app.kubernetes.io/managed-by", "")).lower() == "helm":
        return "helm"
    if any("argocd.argoproj.io" in k for k in keys):
        return "argocd"
    if any("fluxcd.io" in k for k in keys):
        return "flux"
    return "kubectl"


HELM_WARNING = (
    "This resource is Helm-managed (app.kubernetes.io/managed-by=Helm). A live "
    "`kubectl patch` will drift from the chart's rendered manifest and be silently "
    "reverted on the next `helm upgrade` / reconcile. Update the chart's values and run "
    "`helm upgrade` instead of patching live."
)
GITOPS_WARNING_TMPL = (
    "This resource is managed by {tool} (GitOps). A live `kubectl patch` will be "
    "reverted on the next sync. Update the manifest tracked in Git and let {tool} "
    "apply it, instead of patching live."
)


# --------------------------------------------------------------------------------- #
# Field-level patch descriptors — schema-aware replacement for hardcoded command strings
# --------------------------------------------------------------------------------- #
@dataclass(frozen=True)
class FieldPatch:
    """Describes ONE fix at the field level, independent of resource kind. The engine
    resolves it to a concrete, schema-correct patch per target kind (§2)."""
    #: dotted path to the field. For a pod-level patch it is relative to the Pod spec root
    #: (see POD_SPEC_PATH_PREFIX), e.g. "securityContext" or "hostPID". For a
    #: container_scoped patch it is relative to the CONTAINER, e.g.
    #: "securityContext.capabilities.drop".
    path: str
    value: Any
    title: str
    scope: str = "pod-template"        # "pod-template" | "object-root"
    #: True → the field lives on a single container, not the pod spec. The engine emits a
    #: STRATEGIC-MERGE patch keyed by the container's `name` (from the finding's evidence),
    #: e.g. containers:[{name: app, securityContext: {...}}]. This is deliberately NOT an
    #: RFC-6902 `add` at containers[0]: json-add fails when an intermediate object
    #: (securityContext, capabilities, resources) doesn't already exist — the common case —
    #: and containers[0] blindly targets the wrong container in a multi-container pod.
    #: Strategic merge creates the intermediates and merges into the container BY NAME.
    container_scoped: bool = False
    #: Can this exact field be changed on an ALREADY-RUNNING, non-controller-owned Pod?
    #: Almost always False — Pod spec is immutable after creation except for a short,
    #: explicit allow-list (container image, activeDeadlineSeconds, tolerations, ...)
    #: that does not include securityContext / host-namespace fields (§4).
    mutable_on_live_pod: bool = False
    references: tuple = ()


